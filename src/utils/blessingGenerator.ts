/**
 * Get a random biblical blessing
 * @returns A random blessing with text and reference
 */
export const getRandomBlessing = (): Blessing => {
  const randomIndex = Math.floor(Math.random() * BLESSINGS.length);
  return BLESSINGS[randomIndex];
};

/**
 * Format a blessing for display in messages
 * @param blessing The blessing to format
 * @returns Formatted blessing string
 */
export const formatBlessing = (blessing: Blessing): string => {
  return `"${blessing.text}"\n(${blessing.reference})`;
};
